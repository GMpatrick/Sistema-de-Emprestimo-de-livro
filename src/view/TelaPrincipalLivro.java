    public void readTelaPrinLivro(String nome){
        DefaultTableModel modelo = (DefaultTableModel) jTableLivro.getModel();
        modelo.setNumRows(0);
        TelaPrinLivroDao ldao = new TelaPrinLivroDao();
        
        for (TelaPrinLivro l: ldao.readForDes(nome)){
            modelo.addRow(new Object[]{
                l.getId(),
                l.getNome_livro(),
                l.getNome_autor(),
                l.getEditora(),
                l.getData_publicacao(),
                l.getNumero_paginas()
            });
        }
    
    }
